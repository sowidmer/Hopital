package Autres;

public class Lit {
  
  int no;
  boolean medicalise;
  boolean barrierre;
  boolean enfant;
  
  
  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public boolean isMedicalise() {
    return medicalise;
  }
  public void setMedicalise(boolean medicalise) {
    this.medicalise = medicalise;
  }
  public boolean isBarrierre() {
    return barrierre;
  }
  public void setBarrierre(boolean barrierre) {
    this.barrierre = barrierre;
  }
  public boolean isEnfant() {
    return enfant;
  }
  public void setEnfant(boolean enfant) {
    this.enfant = enfant;
  }

  

}
