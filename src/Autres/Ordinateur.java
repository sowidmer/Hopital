package Autres;

public class Ordinateur {
  
  int no;
  String nom;
  int mémoire;
  int noSalle;
  String genre;
  
  
  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public String getNom() {
    return nom;
  }
  public void setNom(String nom) {
    this.nom = nom;
  }
  public int getMémoire() {
    return mémoire;
  }
  public void setMémoire(int mémoire) {
    this.mémoire = mémoire;
  }
  public int getNoSalle() {
    return noSalle;
  }
  public void setNoSalle(int noSalle) {
    this.noSalle = noSalle;
  }
  public String getGenre() {
    return genre;
  }
  public void setGenre(String genre) {
    this.genre = genre;
  }


  
}
