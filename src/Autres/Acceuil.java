package Autres;

public class Acceuil {
  
  int no;
  int noService;
  String secretaire;
  
  
  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public int getNoService() {
    return noService;
  }
  public void setNoService(int noService) {
    this.noService = noService;
  }
  public String getSecretaire() {
    return secretaire;
  }
  public void setSecretaire(String secretaire) {
    this.secretaire = secretaire;
  }


}
