package Autres;

public class Acceuil {

  private int no;
  private int noService;

  public Acceuil(int no, int noService) {
    this.no = no;
    this.noService = noService;
  }

  public int getNo() {
    return no;
  }

  public void setNo(int no) {
    this.no = no;
  }

  public int getNoService() {
    return noService;
  }

  public void setNoService(int noService) {
    this.noService = noService;
  }

}
