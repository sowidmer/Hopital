package locaux;

public class Locaux {
  
  int no;
  String nom;
  String genre;
  int noImmeuble;
  int etage;
  int nbrFenetre;
  int nbrTable;
  int m2;
  
  
  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public String getNom() {
    return nom;
  }
  public void setNom(String nom) {
    this.nom = nom;
  }
  public String getGenre() {
    return genre;
  }
  public void setGenre(String genre) {
    this.genre = genre;
  }
  public int getNoImmeuble() {
    return noImmeuble;
  }
  public void setNoImmeuble(int noImmeuble) {
    this.noImmeuble = noImmeuble;
  }
  public int getEtage() {
    return etage;
  }
  public void setEtage(int etage) {
    this.etage = etage;
  }
  public int getNbrFenetre() {
    return nbrFenetre;
  }
  public void setNbrFenetre(int nbrFenetre) {
    this.nbrFenetre = nbrFenetre;
  }
  public int getNbrTable() {
    return nbrTable;
  }
  public void setNbrTable(int nbrTable) {
    this.nbrTable = nbrTable;
  }
  public int getM2() {
    return m2;
  }
  public void setM2(int m2) {
    this.m2 = m2;
  }
  
  

}
